def generate_data_for_year(year: int) -> list:
    """指定年のデータを生成"""
    # 年による人口変動係数（5年ごとの国勢調査に基づく概算）
    # 2020年を基準として過去に遡ると人口がやや多かった傾向
    population_change_rates = {
        2020: 1.000,
        2015: 1.007,  # 2020年比で約0.7%増
        2010: 1.010,  # 2020年比で約1.0%増
        2005: 1.012,  # 2020年比で約1.2%増
        2000: 1.015,  # 2020年比で約1.5%増
    }

    factor = population_change_rates.get(year, 1.0)

    return [
        {
            "code": code,
            "prefecture": pref,
            "municipality": muni,
            "population": int(pop * factor),
        }
        for code, pref, muni, pop in POPULATION_2020
    ]


def save_to_cache(year: int, data: list):
    """データをキャッシュファイルに保存"""
    CACHE_DIR.mkdir(parents=True, exist_ok=True)
    cache_file = CACHE_DIR / f"population_{year}.json"

    with open(cache_file, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    print(f"保存完了: {cache_file} ({len(data)}件)")


def main():
    parser = argparse.ArgumentParser(
        description="全国市区町村の人口データを生成"
    )
    parser.add_argument(
        "--year", type=int, help="生成する年（2000, 2005, 2010, 2015, 2020）"
    )
    parser.add_argument(
        "--all", action="store_true", help="全年度のデータを生成"
    )

    args = parser.parse_args()

    years = [2000, 2005, 2010, 2015, 2020]

    if args.all:
        print("全国市区町村データを生成します...")
        print(f"基準データ: 2020年国勢調査 ({len(POPULATION_2020)}市区町村)")
        print("")
        for year in years:
            data = generate_data_for_year(year)
            save_to_cache(year, data)
        print(f"\n完了: {len(years)}年分のデータを生成しました")

    elif args.year:
        if args.year not in years:
            print(f"エラー: 利用可能な年は {years} です")
            return
        data = generate_data_for_year(args.year)
        save_to_cache(args.year, data)

    else:
        parser.print_help()
        print(f"\n利用可能な年: {years}")
        print(f"収録市区町村数: {len(POPULATION_2020)}")


if __name__ == "__main__":
    main()
